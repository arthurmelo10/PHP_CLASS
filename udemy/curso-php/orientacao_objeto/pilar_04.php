    <p><strong>Abstração</strong></p>
    <section>
        Mundo Real => aquilo que é a referência para fazer o mapeamento para a aplicação. A partir deste mapeamento, temos a abstração.
        O nível de complexidade de sua aplicação, depende do nível do problema que estamos tentando receber.<br>
        Nunca a complexidade da abstração vai alcançar a complexidade do mundo real, pois alguns fatores presentes no mundo real, não serão levados em conta para a sua aplicação.
        <ul>
            Exemplo: Sistema de Estoque
            <li>Nome do produto</li>
            <li>Quantidade do produto</li>
            <li>Localização do produto</li>
            <li>Dimensões do produto não fazem diferença => <strong>Irrelevante para este cenário</strong></li>
            <br>
            Exemplo 2: Amazon
            <li>Dimensões do produto importam, visto a necessidade de otimização do espaço</li>
            <li>O tipo de material da caixa</li>
            <li>Suas características de resistência</li>
        </ul>

        <h2>TUDO NO MUNDO É UM OBJETO -> PROGRAMAÇÃO COMO FORMA DE PENSAR</h2>

        <p>Este princípio de <b>abstração</b> da OO, tem como objetivo olhar o mundo, olhar as relações entre os objetos (pessoas, processos) e mapear os principais componentes que serão utilizados em sua aplicação.</p>
        <p>Nem tudo que está no mundo real, vai para o software, e nem tudo que está na aplicação, está no mundo real.</p>
    </section>
    <p>
        Demanda do programador, uma habilidade de conversar com o cliente, para entender quais as informações relevantes e necessárias para o seus software. Quais atributos e comportamentos devem estar presentes na minha aplicação
    </p>
    <div>
        
