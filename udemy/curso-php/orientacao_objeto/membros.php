<div class="titulo">Membros</div>

<div>
    <b>Aprofundar nos membros de uma classe, que são </b><br> 
    <li>atributos</li>
    <li>métodos</li>

    <p>
        Na classe, podemos ter variáveis e constantes, que vão ser os atributos de uma classe
    
        <br>
        <strong>Exemplo</strong>

        <b>Classe Produto</b>
        <ul>
            <li>nome - pode ser variável ou constante</li>
            <li>preço - pode ser variável ou constante</li>
            <li>desconto - pode ser variável ou constante</li>
        </ul>
    </p>

    <p>
        Na classe, temos uma parte chamado construtor e uma parte chamada destrutor. O destrutor é chamado quando queremos destruir um método. Quando atrbuímos nulo para um objeto ou chamamos um unset.
        Exemplo: Quero fechar a conexão com o banco de dados.
        Construtor é um método especial, responsável por criar uma nova instãncia da classe.
    </p>

    <p>
        Na classe também temos métodos, que são as ações presentes na classe.
        <br>
        <strong>Exemplo</strong>

        <b>Classe Produto</b>
        <ul>
            <li>nome - pode ser variável ou constante</li>
            <li>preço - pode ser variável ou constante</li>
            <li>desconto - pode ser variável ou constante</li>
            <li>preçoComDesconto - cálculo matemático para aplicar desconto ao preço</li>
        </ul>
        <br>

        Como temos a possibilidade de ter valores padrões, temos a possibilidade de chamar o mesmo método(construtor, destrutor e métodos em gerais).
    </p>

    <p>
        <ul>
            <strong>Atributos</strong>
            <li>Variáveis</li>
            <ul>
                <li>Classe => variáveis que pertencem a classe</li>
                <li>Objeto => variáveis que pertencem ao objeto</li>
            </ul>
            <li>Constantes => const "nome do atributo" </li>
            <ul>
                <li>Classe => constantes que pertencem a classe</li>
                <li>Objeto => constantes que pertencem ao objeto</li>
            </ul>

            <br>
            
            <strong>Métodos</strong>
            <li>Classe => métodos que pertencem a classe</li>
            <li>Objeto => métodos que pertencem ao objeto</li>
            
            <br>
            Métodos, constantes e variáveis que pertencem somente a classe são chamados de <strong>Estáticos !!!</strong>
            <br>
            São conhecidos pela palavra static => definem elementos exclusivos da class_exists


            <p>
                <strong>Acessar os Membros</strong>
                <br>
                <ul>
                    <li>Podemos utilizar a seta = -> Exemplo.: Produto->preço (acessar a propriedade/atributo preço da classe Produto) !! </li>
                    <li>Podemos utilziar "quatro pontos" = :: (elementos estáticos)</li>
                </ul>
            </p>

            <p style="color: red;">
                <strong>
